package corejava.Loops;

public class Do_while {

	public static void main(String[] args) 
	{
		
		/*
		 * Do-While:-->
		 * 		Do while work similar as while loop. Only
		 * 		Differance do-wile execute one iteration
		 * 		even condition was not return true.
		 */
		
		
		int i=100;
		do {
			System.out.println(i);
			i=i+1;
		} while (i<20);
		
		
		
		
		
		
		
		
	}

}
